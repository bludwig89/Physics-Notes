"""
complex_mass_fork.py — Higgs-free complex-mass Dirac CA fork
=============================================================
2026-05-23

Implements Ludwig's 2007 proposal (physics_notes_0708.pdf pp. 59–60):
**gauge the β matrix** of the Dirac equation to produce chiral SU(2)
without a separate Higgs field.

Core idea (pp. 59–60)
---------------------
The standard Dirac equation (Weyl rep) uses

    β = σ₁ ⊗ I_spin                  (mass matrix)

Ludwig observes that β_g = (U σ₁ U†) ⊗ I with

    U = diag(1, e^{iθ})   ∈ U(1) ⊂ SU(2)

gives the same Clifford relations {α_i, β_g}=0, β_g²=I, so it provides a
valid mass coupling for any θ.  Writing this out:

    β_g = cos θ · σ₁ + sin θ · σ₂      (rotation in the σ₁–σ₂ plane)

If θ = θ(x,t) is position-dependent the Dirac equation keeps its form but
the mass coupling becomes *local*.

The gauge action *only on the left-handed (η) half* of the bispinor is
what ties this to the weak interaction, which is also helicity-selective.

The mass coupling in the QCA propagator for this change is:

    D_k(θ) = [[n W_k,          im e^{iθ(x)} I],
               [im e^{-iθ(x)} I,  n W†_k       ]]

which is unitary for any θ(x) — the dispersion ω_k = arccos(n c_x c_y)
is θ-independent (gauge freedom).  This is the 1-flavour version.

SU(2) doublet (the full chiral SU(2) fork)
-------------------------------------------
For a left doublet (ν_L, e_L) and right doublet (ν_R, e_R), replace the
U(1) phase by U(x) ∈ SU(2):

    mass step:   η_new = cos(m) η + i sin(m) U(x) χ
                 χ_new = i sin(m) U†(x) η + cos(m) χ

where U acts on the isospin index and I₂ on the spin index.

Gauge invariance (the chiral SU(2) claim):
Under the *left-handed only* transformation

    η → V(x) η,    χ → χ  (unchanged — RIGHT-HANDED SINGLET)

combined with  U → V U  the mass step is unchanged:

    η_new → V η_new = V(cos η + i sin U χ)
                     = cos(Vη) + i sin (VU χ)    ✓ (same form with U→VU)

    χ_new → cos χ + i sin (VU)† (Vη)
           = cos χ + i sin U†V†Vη = cos χ + i sin U† η  ✓

So the doublet complex-mass is **chiral SU(2) gauge invariant** with NO
Higgs field.  Mass is generated by the frozen gauge orientation of U, which
is pure-gauge — not a physical scalar condensate.

This contradicts the Standard Model story where the Higgs is required to
produce a gauge-invariant fermion mass.

Split-step implementation
-------------------------
For spatially varying θ(x) / U(x), a Fourier-space D_k is not directly
applicable.  We use Strang splitting:

    step(dt) = kinetic(dt/2) ∘ mass(dt) ∘ kinetic(dt/2)

The kinetic half-step is the exact Weyl QCA from ca_core_exact.py applied
independently to each isospin component (no isospin–kinetics coupling).
The mass step is local in position space.
"""

