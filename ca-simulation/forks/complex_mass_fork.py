def chirality_split_doublet(eta_nu_u, eta_nu_d, eta_e_u, eta_e_d,
                            chi_nu_u, chi_nu_d, chi_e_u, chi_e_d):
    """
    Return (N_left, N_right) = total probability in left vs right chirality sectors.
    """
    N_left  = float(sum(np.sum(np.abs(arr)**2)
                        for arr in (eta_nu_u, eta_nu_d, eta_e_u, eta_e_d)))
    N_right = float(sum(np.sum(np.abs(arr)**2)
                        for arr in (chi_nu_u, chi_nu_d, chi_e_u, chi_e_d)))
    return N_left, N_right


def isospin_z_doublet(eta_nu_u, eta_nu_d, eta_e_u, eta_e_d,
                      chi_nu_u, chi_nu_d, chi_e_u, chi_e_d):
    """
    Return ⟨T₃⟩ = (N_ν − N_e) / 2 for left and right sectors separately.

    In SU(2) the isospin-up (ν) component has T₃ = +½ and isospin-down (e)
    has T₃ = −½.  This observable is SU(2)_L invariant only for the
    generator T₃ (the U(1) subgroup).
    """
    N_nu_L = float(sum(np.sum(np.abs(arr)**2) for arr in (eta_nu_u, eta_nu_d)))
    N_e_L  = float(sum(np.sum(np.abs(arr)**2) for arr in (eta_e_u,  eta_e_d)))
    N_nu_R = float(sum(np.sum(np.abs(arr)**2) for arr in (chi_nu_u, chi_nu_d)))
    N_e_R  = float(sum(np.sum(np.abs(arr)**2) for arr in (chi_e_u,  chi_e_d)))

    T3_L = 0.5 * (N_nu_L - N_e_L)
    T3_R = 0.5 * (N_nu_R - N_e_R)
    return T3_L, T3_R


def su2_casimir_left(eta_nu_u, eta_nu_d, eta_e_u, eta_e_d):
    """
    Compute ⟨T²⟩ = T₁² + T₂² + T₃² on the left-handed doublet sector only.

    For an SU(2) doublet state |T=½, T₃=m⟩ the Casimir eigenvalue is
    T(T+1) = ¾.  This function returns the *expectation value* of T² in
    the current left-sector state.

    Using the spin algebra in isospin space:
        T₃ = ½ (|η_ν|² − |η_e|²) / norm
        T+ = sum η_ν* η_e (integrated)
        T- = sum η_e* η_ν (integrated)
        T² = T₃² + ½(T+T- + T-T+)

    The result should be 3/4 if the state is a pure doublet.
    Returns the expectation value (a real float).
    """
    N_nu = float(sum(np.sum(np.abs(arr)**2) for arr in (eta_nu_u, eta_nu_d)))
    N_e  = float(sum(np.sum(np.abs(arr)**2) for arr in (eta_e_u,  eta_e_d)))
    N_total = N_nu + N_e
    if N_total < 1e-30:
        return 0.0

    # T₃² contribution
    T3 = 0.5 * (N_nu - N_e) / N_total
    T3_sq = T3**2

    # T₊ = sum η_ν* η_e (off-diagonal in isospin), T₋ = (T₊)*
    T_plus = (np.sum(np.conj(eta_nu_u) * eta_e_u)
              + np.sum(np.conj(eta_nu_d) * eta_e_d)) / N_total
    T_minus = np.conj(T_plus)

    # ⟨T²⟩ = T₃² + ½(T₊T₋ + T₋T₊) = T₃² + |T₊|²
    T_sq = T3_sq + float(np.abs(T_plus)**2)
    return T_sq
