def rest_leg(phi, c_0, m, mode="exact_isotropic"):
    """Ω_rest(x) = √A(x)·arcsin(m).  Identical functional form to F50."""
    return clock_field(phi, c_0, mode) * float(np.arcsin(m))


def refractive_index(phi, c_0, mode="exact_isotropic", sector="rest_only"):
    """Eikonal refractive index n = c₀ / c_eff for a probe ray.

    sector = "rest_only" : ONLY the rest/clock leg is sourced by mass, the
                           spatial metric stays flat (B = 1).  Then
                           c_eff = c₀√A and  n = 1/√A.
    sector = "full"      : the spatial metric B is ALSO sourced (isotropic GR),
                           c_eff = c₀√(A/B) and  n = √(B/A).
    """
    A, B = forkE.make_fork(mode).metric(np.asarray(phi, dtype=np.float64), c_0)
    A = np.abs(A)
    if sector == "rest_only":
        c_eff = c_0 * np.sqrt(A)            # B := 1
    elif sector == "full":
        c_eff = c_0 * np.sqrt(A / B)
    else:
        raise ValueError("sector must be 'rest_only' or 'full'")
    return c_0 / c_eff


def eikonal_deflection(phi_slice, c_0, axis_long=0, axis_perp=1,
                       impact_index=None, window=None, mode="exact_isotropic",
                       sector="rest_only"):
    """Weak-field eikonal deflection of a straight ray through a 2D potential
    slice.  Ray travels along `axis_long` at a fixed transverse offset
    `impact_index` from the source; deflection α = −∫ ∂_⊥ n  dl   (bends toward
    higher n, i.e. toward the mass).  Returns α (radians, small).

    This is the standard weak-deflection integral; for n − 1 = κ·GM/(r c₀²) it
    evaluates to α = 2κ·GM/(b c₀²), so the coefficient K ≡ α b c₀²/(GM) reads
    off 2κ directly (κ = 1 → Newtonian/factor-1, κ = 2 → Einstein/factor-2).
    """
    phi_slice = np.asarray(phi_slice, dtype=np.float64)
    n = refractive_index(phi_slice, c_0, mode=mode, sector=sector)
    # transverse derivative ∂n/∂(perp), central difference, h = 1
    dn = np.gradient(n, axis=axis_perp)
    Lperp = n.shape[axis_perp]
    if impact_index is None:
        impact_index = Lperp // 2 + 6
    # extract the ray line at the chosen transverse offset
    sl = [slice(None), slice(None)]
    sl[axis_perp] = impact_index
    dn_line = dn[tuple(sl)]
    Llong = dn_line.shape[0]
    if window is None:
        lo, hi = 0, Llong
    else:
        c = Llong // 2
        lo, hi = max(0, c - window), min(Llong, c + window)
    alpha = -np.sum(dn_line[lo:hi])        # ∫ dl with dl = 1 lattice cell
    return float(alpha)


