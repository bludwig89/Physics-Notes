def clock_field(phi, c_0, mode="exact_isotropic"):
    """s(x) = √A(φ(x)) — the rest-leg renormalisation = local clock-tick rate."""
    A, _ = forkE.make_fork(mode).metric(np.asarray(phi, dtype=np.float64), c_0)
