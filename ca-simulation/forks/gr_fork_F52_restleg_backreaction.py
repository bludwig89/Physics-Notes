def relax_restleg_potential(rho, G=1.0, n_iter=6000, tol=1e-8, log_every=0):
    """LITERAL fixed point: solve ∇²Φ = 4πG ρ by hand-rolled Jacobi relaxation
    on a 6-neighbour 3D stencil (h = 1) with Φ = 0 Dirichlet boundary (an
    isolated mass, field → 0 far away).

    This is the "relax the clock-rate field to equilibrium" reading of the
    hypothesis, written from scratch (per project practice) rather than via an
    FFT, so we can watch it converge to the same field the spectral solver
    gives.  Returns (Phi, final_residual, iters_run).
    """
    rho = np.asarray(rho, dtype=np.float64)
    src = 4.0 * np.pi * G * rho            # RHS of ∇²Φ = src
    Phi = np.zeros_like(rho)
    res = np.inf
    iters = 0
    for it in range(n_iter):
        # 6-neighbour sum via roll (periodic roll, then re-impose Dirichlet)
        nb = (np.roll(Phi, 1, 0) + np.roll(Phi, -1, 0) +
              np.roll(Phi, 1, 1) + np.roll(Phi, -1, 1) +
              np.roll(Phi, 1, 2) + np.roll(Phi, -1, 2))
        Phi_new = (nb - src) / 6.0
        # Dirichlet Φ=0 on the box faces
        Phi_new[0, :, :] = Phi_new[-1, :, :] = 0.0
        Phi_new[:, 0, :] = Phi_new[:, -1, :] = 0.0
        Phi_new[:, :, 0] = Phi_new[:, :, -1] = 0.0
        res = float(np.max(np.abs(Phi_new - Phi)))
        Phi = Phi_new
        iters = it + 1
        if log_every and (it % log_every == 0):
            print(f"    relax it={it:5d}  max|ΔΦ|={res:.3e}")
        if res < tol:
            break
    return Phi, res, iters


